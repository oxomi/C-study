#include <stdio.h>

//10과 20을 더하는 프로그램

int main(void)
{
	10 + 20;

	return 0;
}




int main(void)
{
	printf("Be happy");
	printf("My friend");

	return 0;
}




int main(void)
{
	printf("%d\n", 10);
	printf("%.4lf\n", 3.4);

	printf("%d과 %d의 합은 %d입니다.", 10, 20, 10 + 20);

	return 0;
}



int main(void)
{
	printf("%d를 %d로 나누면 %.6lf입니다.", 1, 2, 0.5);
	return 0;
}


int main(void)
{
	printf("Be\rHappy!\nBaby");
	return 0;
}


